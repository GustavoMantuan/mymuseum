    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        manutencaomaterial.setCd_material(Integer.parseInt(codmaterial.getText()));
        manutencaomaterial.setQt_material(Integer.parseInt(psmaterial.getText()));
        String[] obr = cd_obra.getSelectedItem().toString().split("-");
        String titulo = obr[1].toString();
        obra.setTi_obra(titulo);
        daoobra.consultaObra(obra);
        try {
            obra.getRetorno().first();
            int cdobra = obra.getRetorno().getInt("CD_OBRA");
            manutencaomaterial.setCd_obra(cdobra);
            manutencaomaterial.setTp_obra(obra.getRetorno().getInt("TP_OBRA"));
        } catch (SQLException ex) {
            Logger.getLogger(GerenciarManutencao.class.getName()).log(Level.SEVERE, null, ex);
        }
        manutencaomaterial.setTabela(jTable2);
        daomanutencaomaterial.incluiritens(manutencaomaterial);
        
        codmaterial.setText("");
        psmaterial.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

